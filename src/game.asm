GetRandomNumber:
; Uses a 16-bit RAM variable called RandomNumberGeneratorWord
; Returns an 8-bit pseudo-random number in a
    push hl
        ld hl,(RandomNumberGeneratorWord)
        ld a,h         ; get high byte
        rrca           ; rotate right by 2
        rrca
        xor h          ; xor with original
        rrca           ; rotate right by 1
        xor l          ; xor with low byte
        rrca           ; rotate right by 4
        rrca
        rrca
        rrca
        xor l          ; xor again
        rra            ; rotate right by 1 through carry
        adc hl,hl      ; add RandomNumberGeneratorWord to itself
        jr nz,+
        ld hl,$733c    ; if last xor resulted in zero then re-seed random number generator
+:      ld a,r         ; r = refresh register = semi-random number
        xor l          ; xor with l which is fairly random
        ld (RandomNumberGeneratorWord),hl
    pop hl
    ret                ; return random number in a

;==============================================================
; Music engine
;==============================================================
; Data format: 4 bytes per note (psg_tone_latch, psg_tone_data, psg_volume, duration)
; $FF = loop back to start
;
; SMS PSG (SN76489) tone values = 3579545 / (32 * freq_hz)
; Volume: $9x for ch0, $Bx for ch1 (x=0 loudest, x=F mute)

InitMusic:
    ld hl,MelodyData
    ld (Ch0Ptr),hl
    ld hl,BassData
    ld (Ch1Ptr),hl
    ld hl,HarmonyData
    ld (Ch2Ptr),hl
    ld a,1
    ld (Ch0Timer),a
    ld (Ch1Timer),a
    ld (Ch2Timer),a
    ret

UpdateMusic:
    ; --- Channel 0 (melody) ---
    ld a,(Ch0Timer)
    dec a
    ld (Ch0Timer),a
    or a
    jr nz,_ch1

    ld hl,(Ch0Ptr)
_ch0Read:
    ld a,(hl)
    cp $FF
    jr nz,+
    ld hl,MelodyData
    jr _ch0Read
+:
    out ($7F),a         ; tone latch (channel + low 4 bits)
    inc hl
    ld a,(hl)
    out ($7F),a         ; tone data (high 6 bits)
    inc hl
    ld a,(hl)
    out ($7F),a         ; volume
    inc hl
    ld a,(hl)
    ld (Ch0Timer),a     ; duration in frames
    inc hl
    ld (Ch0Ptr),hl

_ch1:
    ; --- Channel 1 (bass) ---
    ld a,(Ch1Timer)
    dec a
    ld (Ch1Timer),a
    or a
    jr nz,_ch2

    ld hl,(Ch1Ptr)
_ch1Read:
    ld a,(hl)
    cp $FF
    jr nz,+
    ld hl,BassData
    jr _ch1Read
+:
    out ($7F),a         ; tone latch
    inc hl
    ld a,(hl)
    out ($7F),a         ; tone data
    inc hl
    ld a,(hl)
    out ($7F),a         ; volume
    inc hl
    ld a,(hl)
    ld (Ch1Timer),a     ; duration
    inc hl
    ld (Ch1Ptr),hl

_ch2:
    ; --- Channel 2 (harmony) ---
    ld a,(Ch2Timer)
    dec a
    ld (Ch2Timer),a
    or a
    ret nz

    ld hl,(Ch2Ptr)
_ch2Read:
    ld a,(hl)
    cp $FF
    jr nz,+
    ld hl,HarmonyData
    jr _ch2Read
+:
    out ($7F),a         ; tone latch
    inc hl
    ld a,(hl)
    out ($7F),a         ; tone data
    inc hl
    ld a,(hl)
    out ($7F),a         ; volume
    inc hl
    ld a,(hl)
    ld (Ch2Timer),a     ; duration
    inc hl
    ld (Ch2Ptr),hl

    ret

