;==============================================================
; WLA-DX banking setup
;==============================================================
.memorymap
defaultslot 0
slotsize $8000
slot 0 $0000
slot 1 $c000  ; RAM slot (SMS RAM is $C000-$DFFF)
.endme

.rombankmap
bankstotal 1
banksize $8000
banks 1
.endro

.ramsection "Variables" slot 1
RandomNumberGeneratorWord dw
Ch0Ptr dw
Ch0Timer db
Ch1Ptr dw
Ch1Timer db
Ch2Ptr dw
Ch2Timer db
.ends

;==============================================================
; SDSC tag and SMS rom header
;==============================================================
.sdsctag 1.2,"Hello World!","SMS programming tutorial program","Maxim"

.bank 0 slot 0
.org $0000
;==============================================================
; Boot section
;==============================================================
    di              ; disable interrupts
    im 1            ; Interrupt mode 1
    jp main         ; jump to main program

.org $0038
;==============================================================
; VBlank interrupt handler
;==============================================================
    push af
    push bc
    push hl
    in a,($bf)          ; acknowledge VBlank interrupt
    call UpdateMusic
    pop hl
    pop bc
    pop af
    ei
    reti

.org $0066
;==============================================================
; Pause button handler
;==============================================================
    ; Do nothing
    retn

;==============================================================
; Main program
;==============================================================
main:
    ld sp, $dff0

    ;==============================================================
    ; Set up VDP registers
    ;==============================================================
    ld hl,VdpData
    ld b,VdpDataEnd-VdpData
    ld c,$bf
    otir

    ;==============================================================
    ; Clear VRAM
    ;==============================================================
    ; 1. Set VRAM write address to 0 by outputting $4000 ORed with $0000
    ld a,$00
    out ($bf),a
    ld a,$40
    out ($bf),a
    ; 2. Output 16KB of zeroes
    ld bc, $4000    ; Counter for 16KB of VRAM
    ClearVRAMLoop:
        ld a,$00    ; Value to write
        out ($be),a ; Output to VRAM address, which is auto-incremented after each write
        dec bc
        ld a,b
        or c
        jp nz,ClearVRAMLoop

    ;==============================================================
    ; Load palette
    ;==============================================================
    ; 1. Set VRAM write address to CRAM (palette) address 0 (for palette index 0)
    ; by outputting $c000 ORed with $0000
    ld a,$00
    out ($bf),a
    ld a,$c0
    out ($bf),a
    ; 2. Output colour data
    ld hl,PaletteData
    ld b,(PaletteDataEnd-PaletteData)
    ld c,$be
    otir

    ;==============================================================
    ; Load tiles (font)
    ;==============================================================
    ; 1. Set VRAM write address to tile index 0
    ; by outputting $4000 ORed with $0000
    ld a,$00
    out ($bf),a
    ld a,$40
    out ($bf),a
    ; 2. Output tile data
    ld hl,FontData              ; Location of tile data
    ld bc,FontDataEnd-FontData  ; Counter for number of bytes to write
    WriteTilesLoop:
        ; Output data byte then three zeroes, because our tile data is 1 bit
        ; and must be increased to 4 bit
        ld a,(hl)        ; Get data byte
        out ($be),a
        inc hl           ; Add one to hl so it points to the next data byte
        dec bc
        ld a,b
        or c
        jp nz,WriteTilesLoop

    ;==============================================================
    ; Write text to name table
    ;==============================================================
    ; Line 1: "Ponny's SMS roguelike!" at row 0, col 5
    ld a,$0A             ; low byte of $3800 + 5*2
    out ($bf),a
    ld a,$38|$40         ; high byte with write flag
    out ($bf),a
    ld hl,Message1
    ld bc,Message1End-Message1
    _writeText1:
        ld a,(hl)
        out ($be),a
        inc hl
        dec bc
        ld a,b
        or c
        jp nz,_writeText1

    ; Line 2: "HAI BRAILLYNN!" at row 4, col 9
    ld a,$12             ; low byte of $3800 + 4*64 + 9*2
    out ($bf),a
    ld a,$39|$40         ; high byte with write flag
    out ($bf),a
    ld hl,Message2
    ld bc,Message2End-Message2
    _writeText2:
        ld a,(hl)
        out ($be),a
        inc hl
        dec bc
        ld a,b
        or c
        jp nz,_writeText2

    ; Turn screen on with VBlank interrupts enabled
    ld a,%11100000
;          |||| |`- Zoomed sprites -> 16x16 pixels
;          |||| `-- Doubled sprites -> 2 tiles per sprite, 8x16
;          |||`---- 30 row/240 line mode
;          ||`----- 28 row/224 line mode
;          |`------ VBlank interrupts (bit 5)
;          `------- Enable display (bit 6)
    out ($bf),a
    ld a,$81
    out ($bf),a

    ; Initialize music and enable interrupts
    call InitMusic
    ei

Loop:
    halt                ; wait for VBlank
    jp Loop

